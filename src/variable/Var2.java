package variable;

public class Var2 {
    public static void main(String[] args) {
        int a; // 변수 선언 : 변수를 만드는 것을 변수 선언 이라 함.
        a = 10; // 변수 초가화 : 선언한 변수에 처음으로 값을 대입해서 저장하는 것을 변수 초기화라 함.
        // java 에서 = 는 오른쪽에 았는 값을 왼쪽에 저장한다. 수학에서의 = 와는 다름!
        System.out.println(a); // 변수 a의 값을 읽음
        System.out.println(a);
        System.out.println(a);
    }
}
