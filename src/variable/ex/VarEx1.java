package variable.ex;

public class VarEx1 {
    public static void main(String[] args) {

        /*
        문제1.
        다음코드에 반복해서 나오는 숫자 `4` ,`3` 을 다른 숫자로 한번에 변경 할 수 있도록 다음을 변수 `num1` , `num2` 를 사용하도록 변경해보시오.

        System.out.println(4 + 3);
        System.out.println(4 - 3);
        System.out.println(4 * 3);
        */

        int num1 = 4;
        int num2 = 3;
        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
    }
}
