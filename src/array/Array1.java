package array;

public class Array1 {
    public static void main(String[] args) {
        int student1 = 90;
        int student2 = 80;
        int student3 = 70;
        int student4 = 60;
        int student5 = 50;

        System.out.println("학생1 점수: " + student1);
        System.out.println("학생2 점수: " + student2);
        System.out.println("학생3 점수: " + student3);
        System.out.println("학생4 점수: " + student4);
        System.out.println("학생5 점수: " + student5);

        /*
        - 학생 수가 증가함에 따라 코딩 양이 비례해서 증가하는 문제가 발생한다.

        - 문자는 더하거니 뺄수 있지만, 변수는 더하거나 뺼수 가 없다. ->
          반복문으로 해결할 수 있을 것 같지만, 점수를 출력하는 부분을 보면 변수 이름이 다르기 때문에 적용 할 수 없다.
         */
    }
}
